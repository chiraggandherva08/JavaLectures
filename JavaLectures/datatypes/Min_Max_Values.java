package datatypes;

public class Min_Max_Values {

    public static void main(String[] args) {

        // Min and Max values of Int :
        System.out.println( "\nMax value of Int: " + Integer.MAX_VALUE );
        System.out.println( "Min value of Int: " + Integer.MIN_VALUE );

        // Min and Max value of Float :
        System.out.println( "\nMax value of Float: " + Float.MAX_VALUE );
        System.out.println( "Min value of Float: " + Float.MIN_VALUE );

        // Min and Max value of Double :
        System.out.println( "\nMax value of Double: " + Double.MAX_VALUE );
        System.out.println( "Min value of Double: " + Double.MIN_VALUE );

        // Min and Max value of Short :
        System.out.println( "\nMax value of Short: " + Short.MAX_VALUE );
        System.out.println( "Min value of Short: " + Short.MIN_VALUE );

        // Min and Max value of Byte :
        System.out.println( "\nMax value of Byte: " + Byte.MAX_VALUE );
        System.out.println( "Min value of Byte: " + Byte.MIN_VALUE );

        // Min and Max value of Long :
        System.out.println( "\nMax value of Long: " + Long.MAX_VALUE );
        System.out.println( "Min value of Long: " + Long.MIN_VALUE );

        // Min and Max value of Char :
        System.out.println( "\nMax value of Char: " + Character.MAX_VALUE );
        System.out.println( "Min value of Char: " + Character.MIN_VALUE );

    }
}
