package datatypes;

public class CharacterArithmetic {

    public static void main(String[] args) {
        /*  if we add 1 to a character we get the next character
            eg- 'a'++ will be 'b'
                'A'++ will be 'B'
                'b'++ will be 'c'   */

        /*  we can also give the character datatype value using its ascii value */
        char asciiFromChar = 99;
        System.out.println(asciiFromChar);

    }
}
